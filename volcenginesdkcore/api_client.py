class ApiClient(object):
    """Generic API client for Swagger client library builds.

    Swagger generic API client. This client handles the client-
    server communication, and is invariant across implementations. Specifics of
    the methods and models for each application are generated from the Swagger
    templates.

    NOTE: This class is auto generated by the swagger code generator program.
    Ref: https://github.com/swagger-api/swagger-codegen
    Do not edit the class manually.

    :param configuration: .Configuration object for this client
    :param header_name: a header to pass when making calls to the API.
    :param header_value: a header value to pass when making calls to
        the API.
    :param cookie: a cookie to include in the header when making calls
        to the API
    """

    PRIMITIVE_TYPES = (float, bool, bytes, six.text_type) + six.integer_types
    NATIVE_TYPES_MAPPING = {
        'int': int,
        'long': int if six.PY3 else long,  # noqa: F821
        'float': float,
        'str': str,
        'bool': bool,
        'date': datetime.date,
        'datetime': datetime.datetime,
        'object': object,
    }

    def __init__(self, configuration=None, header_name=None, header_value=None,
                 cookie=None):
        if configuration is None:
            configuration = Configuration()
        self.configuration = configuration

        # Use the pool property to lazily initialize the ThreadPool.
        self._pool = None
        self.rest_client = rest.RESTClientObject(configuration)
        self.default_headers = {}
        if header_name is not None:
            self.default_headers[header_name] = header_value
        self.cookie = cookie
        # Set default User-Agent.
        self.user_agent = 'volcstack-python-sdk/5.0.29'
        self.client_side_validation = configuration.client_side_validation

        self.interceptor_chain = InterceptorChain()

        self.interceptor_chain.append_request_interceptor(BuildRequestInterceptor())
        self.interceptor_chain.append_request_interceptor(RuntimeOptionsInterceptor())
        self.interceptor_chain.append_request_interceptor(ResolveEndpointInterceptor())
        self.interceptor_chain.append_request_interceptor(SignRequestInterceptor())

        self.interceptor_chain.append_response_interceptor(DeserializedResponseInterceptor())

    def __del__(self):
        if self._pool is not None:
            self._pool.close()
            self._pool.join()

    @property
    def pool(self):
        if self._pool is None:
            self._pool = ThreadPool()
        return self._pool

    @property
    def user_agent(self):
        """User agent for this API client"""
        return self.default_headers['User-Agent']

    @user_agent.setter
    def user_agent(self, value):
        self.default_headers['User-Agent'] = value

    def set_default_header(self, header_name, header_value):
        self.default_headers[header_name] = header_value

    def __call_api(
            self, resource_path, method, path_params=None,
            query_params=None, header_params=None, body=None, post_params=None,
            files=None, response_type=None, auth_settings=None,
            _return_http_data_only=None, collection_formats=None,
            _preload_content=True, _request_timeout=None):

        # header parameters
        header_params = header_params or {}
        header_params.update(self.default_headers)
        if self.cookie:
            header_params['Cookie'] = self.cookie

        interceptor_context = InterceptorContext(request=Request(
            self.configuration,
            resource_path, method, path_params,
            query_params, header_params, body, post_params,
            files, response_type, auth_settings,
            _return_http_data_only, collection_formats,
            _preload_content, _request_timeout,
        ))

        interceptor_context = self.interceptor_chain.execute_request(interceptor_context)

        self._log_config(interceptor_context.get_request())

        retry_count = 0
        response_data = None
        retry_err = None

        auto_retry = interceptor_context.request.auto_retry
        retryer = interceptor_context.request.retryer
        num_max_retries = retryer.num_max_retries

        while True:
            # perform request and return response
            try:
                response_data = self.request(
                    method, url=interceptor_context.request.url, query_params=interceptor_context.request.query_params,
                    headers=interceptor_context.request.header_params,
                    post_params=interceptor_context.request.post_params, body=interceptor_context.request.body,
                    _preload_content=interceptor_context.request.preload_content,
                    _request_timeout=interceptor_context.request.request_timeout)
                self.last_response = response_data
            except Exception as e:
                sdk_core_logger.debug("request error: {}".format(e))
                retry_err = e
                if retry_count >= num_max_retries:
                    raise e

            if not auto_retry or not self.request_retry(response_data, retry_count, retry_err, retryer):
                if retry_err is not None:
                    raise retry_err
                break
            retry_count += 1
            retry_err = None

        interceptor_context.response = Response(response_data)
        interceptor_context = self.interceptor_chain.execute_response(interceptor_context)

        response = interceptor_context.response
        if _return_http_data_only:
            return response.result
        else:
            return (response.result, response.http_response.status,
                    response.http_response.getheaders())

    def request_retry(self, response_data, retry_count, retry_err, retryer):
        if retryer.should_retry(response_data, retry_count, retry_err):
            delay = retryer.get_backoff_delay(retry_count)
            sleep(delay / 1000)
            if self.configuration.debug:
                sdk_core_logger.debug_config(
                    "retry backoff strategy:%s, retry condition: %s, max retry count:%d, current retry count: %d, retry delay(ms):%f",
                    type(retryer.backoff_strategy).__name__, type(retryer.retry_condition).__name__,
                    retryer.num_max_retries, retry_count + 1, delay)
            return True
        return False

    def call_api(self, resource_path, method,
                 path_params=None, query_params=None, header_params=None,
                 body=None, post_params=None, files=None,
                 response_type=None, auth_settings=None, async_req=None,
                 _return_http_data_only=None, collection_formats=None,
                 _preload_content=True, _request_timeout=None):
        """Makes the HTTP request (synchronous) and returns deserialized data.

        To make an async request, set the async_req parameter.

        :param resource_path: Path to method endpoint.
        :param method: Method to call.
        :param path_params: Path parameters in the url.
        :param query_params: Query parameters in the url.
        :param header_params: Header parameters to be
            placed in the request header.
        :param body: Request body.
        :param post_params dict: Request post form parameters,
            for `application/x-www-form-urlencoded`, `multipart/form-data`.
        :param auth_settings list: Auth Settings names for the request.
        :param response: Response data type.
        :param files dict: key -> filename, value -> filepath,
            for `multipart/form-data`.
        :param async_req bool: execute request asynchronously
        :param _return_http_data_only: response data without head status code
                                       and headers
        :param collection_formats: dict of collection formats for path, query,
            header, and post parameters.
        :param _preload_content: if False, the urllib3.HTTPResponse object will
                                 be returned without reading/decoding response
                                 data. Default is True.
        :param _request_timeout: timeout setting for this request. If one
                                 number provided, it will be total request
                                 timeout. It can also be a pair (tuple) of
                                 (connection, read) timeouts.
        :return:
            If async_req parameter is True,
            the request will be called asynchronously.
            The method will return the request thread.
            If parameter async_req is False or missing,
            then the method will return the response directly.
        """
        if not async_req:
            return self.__call_api(resource_path, method,
                                   path_params, query_params, header_params,
                                   body, post_params, files,
                                   response_type, auth_settings,
                                   _return_http_data_only, collection_formats,
                                   _preload_content, _request_timeout)
        else:
            thread = self.pool.apply_async(self.__call_api, (resource_path,
                                                             method, path_params, query_params,
                                                             header_params, body,
                                                             post_params, files,
                                                             response_type, auth_settings,
                                                             _return_http_data_only,
                                                             collection_formats,
                                                             _preload_content, _request_timeout))
        return thread

    def request(self, method, url, query_params=None, headers=None,
                post_params=None, body=None, _preload_content=True,
                _request_timeout=None):
        """Makes the HTTP request using RESTClient."""
        if method == "GET":
            return self.rest_client.GET(url,
                                        query_params=query_params,
                                        _preload_content=_preload_content,
                                        _request_timeout=_request_timeout,
                                        headers=headers)
        elif method == "HEAD":
            return self.rest_client.HEAD(url,
                                         query_params=query_params,
                                         _preload_content=_preload_content,
                                         _request_timeout=_request_timeout,
                                         headers=headers)
        elif method == "OPTIONS":
            return self.rest_client.OPTIONS(url,
                                            query_params=query_params,
                                            headers=headers,
                                            post_params=post_params,
                                            _preload_content=_preload_content,
                                            _request_timeout=_request_timeout,
                                            body=body)
        elif method == "POST":
            return self.rest_client.POST(url,
                                         query_params=query_params,
                                         headers=headers,
                                         post_params=post_params,
                                         _preload_content=_preload_content,
                                         _request_timeout=_request_timeout,
                                         body=body)
        elif method == "PUT":
            return self.rest_client.PUT(url,
                                        query_params=query_params,
                                        headers=headers,
                                        post_params=post_params,
                                        _preload_content=_preload_content,
                                        _request_timeout=_request_timeout,
                                        body=body)
        elif method == "PATCH":
            return self.rest_client.PATCH(url,
                                          query_params=query_params,
                                          headers=headers,
                                          post_params=post_params,
                                          _preload_content=_preload_content,
                                          _request_timeout=_request_timeout,
                                          body=body)
        elif method == "DELETE":
            return self.rest_client.DELETE(url,
                                           query_params=query_params,
                                           headers=headers,
                                           _preload_content=_preload_content,
                                           _request_timeout=_request_timeout,
                                           body=body)
        else:
            raise ValueError(
                "http method must be `GET`, `HEAD`, `OPTIONS`,"
                " `POST`, `PATCH`, `PUT` or `DELETE`."
            )

    def select_header_accept(self, accepts):
        """Returns `Accept` based on an array of accepts provided.

        :param accepts: List of headers.
        :return: Accept (e.g. application/json).
        """
        if not accepts:
            return

        accepts = [x.lower() for x in accepts]

        if 'application/json' in accepts:
            return 'application/json'
        else:
            return ', '.join(accepts)

    def select_header_content_type(self, content_types):
        """Returns `Content-Type` based on an array of content_types provided.

        :param content_types: List of content-types.
        :return: Content-Type (e.g. application/json).
        """
        if not content_types:
            return 'application/json'

        content_types = [x.lower() for x in content_types]

        if 'application/json' in content_types or '*/*' in content_types:
            return 'application/json'
        else:
            return content_types[0]

    def _log_config(self, request):

        if not sdk_core_logger.is_enabled_for_loglevel(LogLevel.LOG_DEBUG_WITH_CONFIG):
            return

        sb = []

        sb.append("SDK Version        : ")
        sb.append(self.user_agent + "\n")

        # 连接池配置
        sb.append("[Connection Pool]" + "\n")
        sb.append("  Number of pools           : ")
        sb.append(str(self.configuration.num_pools) + "\n")
        sb.append("  Connection pool maxsize   : ")
        sb.append(str(self.configuration.connection_pool_maxsize) + "\n")

        # SSL设置
        sb.append("[Scheme]" + "\n")
        sb.append("  Scheme      : ")
        sb.append(str(request.scheme) + "\n")

        # 代理设置（隐藏部分信息避免敏感泄露）
        sb.append("[Proxy]" + "\n");
        sb.append("  HTTP Proxy       : ")
        sb.append(str(self.configuration.http_proxy) + "\n")
        sb.append("  HTTPS Proxy      : ")
        sb.append(str(self.configuration.https_proxy) + "\n")

        # 超时设置
        sb.append("[Timeout]" + "\n");
        sb.append("  Connect Timeout(ms)  : ")
        sb.append(str(self.configuration.connect_timeout) + "\n")
        sb.append("  Read Timeout(ms)     : ")
        sb.append(str(self.configuration.read_timeout) + "\n")

        # 重试设置
        sb.append("[Retry]" + "\n");
        sb.append("  Auto Retry       : ")
        sb.append(str(request.auto_retry) + "\n")
        if request.auto_retry and request.retryer is not None:
            sb.append("  Max Retries      : ")
            sb.append(str(request.retryer.num_max_retries) + "\n")
            sb.append("  Min Delay (ms)   : ")
            sb.append(str(request.retryer.backoff_strategy.min_retry_delay_ms) + "\n")
            sb.append("  Max Delay (ms)   : ")
            sb.append(str(request.retryer.backoff_strategy.max_retry_delay_ms) + "\n")
            sb.append("  Retry Condition  : ")
            sb.append(type(request.retryer.retry_condition).__name__ if request.retryer.retry_condition is not None else "None" + "\n")
            sb.append("  Backoff Strategy : ")
            sb.append(type(request.retryer.backoff_strategy).__name__ if request.retryer.backoff_strategy is not None else "None" + "\n")
            sb.append("  Retry ErrorCodes : ")
            sb.append(str(request.retryer.retry_condition.retry_error_codes) + "\n")

        # EndpointResolver设置
        sb.append("[Endpoint Resolver]" + "\n")
        sb.append("  Region           : ")
        sb.append(str(request.region) + "\n")
        sb.append("  Endpoint         : ")
        sb.append(str(request.host) + "\n")
        sb.append("  Use DualStack    : ")
        sb.append(str(request.use_dual_stack) + "\n")
        sb.append("  Bootstrap Region : ")
        sb.append(str(request.custom_bootstrap_region) + "\n")
        sb.append("  Resolver Class   : ")
        sb.append(type(request.endpoint_provider).__name__ if request.endpoint_provider is not None else "None" + "\n")

        sdk_core_logger.debug_config("".join(sb))

    def _sts_call(self, action, version, params,
                  host=None, scheme='https', region='cn-beijing',
                  timeout=30, service='sts',
                  max_retries=3, retry_interval=1,
                  provider_name="ApiClient"):
        """Invoke an STS federation endpoint (AssumeRoleWithOIDC / AssumeRoleWithSAML / etc.)
        through the SDK's main REST stack while explicitly disabling signing.

        This reuses `ApiClient.call_api` so TLS config (`verify_ssl`,
        `ssl_ca_cert`, `cert_file`, `assert_hostname`), proxy (`http_proxy` /
        `https_proxy` / env vars), connect/read timeouts, connection pooling
        and observability are all honored exactly as for any other SDK API call.

        NOTE: this method dispatches via a NEW `ApiClient(fresh)` (not via
        `self`); changes to `ApiClient.__init__` / `call_api` / interceptors
        affect this STS path too.

        Signing is skipped by passing `auth_settings=None`, which causes
        `SignRequestInterceptor.update_params_for_auth` to early-return. This
        is correct because OIDC / SAML federation entry points are unsigned
        by design (the OIDCToken / SAMLResp is the authentication material).

        Recursion is broken by clearing `credential_provider` and setting
        `ak` / `sk` to sentinel values on the fresh Configuration copy, so
        the STS request does not itself invoke the provider that initiated
        it.

        `provider_name` is used only for log/error context (e.g.
        "STSOIDCProvider") — pass the calling provider's PROVIDER_NAME so
        operators can trace which credential source initiated the STS call.

        Returns the unwrapped `Result` dict from the STS response. Business
        errors bubble up as `rest.ApiException` (matching master behavior).
        """
        # Configuration() via the TypeWithDefault metaclass returns a shallow
        # copy of the user's default configuration, so TLS/proxy/timeout
        # settings propagate to the STS call.
        fresh = Configuration()
        # Break recursion on three fronts:
        #   1. credential_provider=None so SignRequestInterceptor won't call
        #      back into the provider via get_credentials().
        #   2. Non-empty ak/sk sentinel so the feature branch's
        #      SignRequestInterceptor does NOT auto-attach a
        #      DefaultCredentialProvider (which would recurse through the
        #      whole credential chain). The sentinel never reaches the wire
        #      because auth_settings=None short-circuits
        #      update_params_for_auth before signing.
        fresh.credential_provider = None
        fresh.ak = "_sts_bootstrap_unsigned"
        fresh.sk = "_sts_bootstrap_unsigned"
        fresh.session_token = ""
        # Per-call overrides from the caller
        if host:
            fresh.host = host
        fresh.scheme = scheme
        fresh.region = region
        fresh.read_timeout = timeout

        # Retry: apply caller-level retry semantics to this STS call only.
        # Deep-copy the retryer so we don't mutate the caller's default
        # retryer (DEFAULT_RETRYER is a module-level singleton shared across
        # Configurations). Configuration exposes retryer via a getter-only
        # property; swap the underlying __retryer via name mangling.
        fresh._Configuration__retryer = copy.deepcopy(fresh.retryer)
        # Caller semantics: max_retries = TOTAL attempts (legacy from
        # _do_http_request's for-loop). Configuration semantics:
        # num_max_retries = retries AFTER the initial attempt. Convert.
        fresh.num_max_retries = max(max_retries - 1, 0)
        # retry_interval is seconds; Configuration uses ms.
        # Collapse the exponential-backoff range to a fixed delay.
        delay_ms = int(retry_interval * 1000)
        fresh.min_retry_delay_ms = delay_ms
        fresh.max_retry_delay_ms = delay_ms

        # NOTE: NEW ApiClient(fresh), not self — avoids re-entering caller's provider chain.
        client = ApiClient(fresh)
        # BuildRequestInterceptor parses resource_path by index:
        # [1]=Action [2]=Version [3]=service
        resource_path = '/{}/{}/{}/post/'.format(action, version, service)
        logger.debug(
            "%s: STS call %s %s region=%s host=%s",
            provider_name, action, version, region, host or fresh.host,
        )
        return client.call_api(
            resource_path,
            'POST',
            path_params={},
            query_params=[],
            header_params={
                'Accept': 'application/json',
                'Content-Type': 'application/x-www-form-urlencoded',
            },
            body=params,
            post_params=[],
            files={},
            response_type=object,
            auth_settings=None,            # skip signing
            async_req=False,
            _return_http_data_only=True,
            _preload_content=True,
            collection_formats={},
        )

    def _do_http_request(self, url, method="GET", data=None, headers=None, timeout=None,
                         max_retries=1, retry_interval=0, request_name=None,
                         retry_on_5xx=True, provider_name="ApiClient"):
        """Invoke an arbitrary HTTP(S) endpoint through the SDK's RESTClient.

        Routing through `RESTClientObject` (constructed from `Configuration()`)
        ensures the transport honors the same TLS/proxy settings as every
        other SDK API call:
          - `verify_ssl`, `ssl_ca_cert`, `cert_file`, `key_file`,
            `assert_hostname`
          - `proxy` / `http_proxy` / `https_proxy` (plus HTTP_PROXY /
            HTTPS_PROXY environment variables)
          - `connect_timeout` / `read_timeout` / connection pool sizing

        Unlike `_sts_call` (which goes through `ApiClient.call_api`), this
        path skips the OpenAPI semantics layer (signing, Action/Version
        path parsing, response unwrapping) -- it's meant for non-OpenAPI
        endpoints such as the SSO OAuth token endpoint, the SSO Portal,
        and ECS IMDSv2.

        Args:
          url: Fully-qualified request URL. Query string may be pre-embedded;
               RESTClient will not second-encode it.
          method: HTTP method.
          data: Request body. When `Content-Type` is JSON, RESTClient will
                `json.dumps` a dict automatically; pass a str/bytes to
                bypass. For GET/HEAD, pass None.
          headers: Request headers dict.
          timeout: Total request timeout (seconds). Passed as
                   `_request_timeout` to RESTClient.
          max_retries: Total number of attempts (including the first).
          retry_interval: Seconds to sleep between retries.
          request_name: Human-readable name used in error messages.
          retry_on_5xx: If True (default) retry on HTTP 5xx responses. Pass
                        False for non-idempotent POSTs where replay may be
                        unsafe (e.g. OAuth refresh_token grants that rotate
                        the refresh token on use).
          provider_name: Used only for log/error context (e.g.
                         "ECSRoleProvider"). Pass the calling provider's
                         PROVIDER_NAME so operators can trace which
                         credential source initiated the request.

        Returns:
          Response body as a decoded str.

        Raises:
          RuntimeError on non-2xx HTTP response or transport failure.
        """
        # Late import for urllib3 (small dep, only needed in this method).
        import urllib3
        from volcenginesdkcore.rest import RESTClientObject, ApiException

        attempts = max(max_retries, 1)
        # Configuration() returns a shallow copy of the user's default config
        # via TypeWithDefault, so TLS/proxy/timeout settings propagate.
        rest_client = RESTClientObject(Configuration())

        last_error = None
        for attempt in range(attempts):
            try:
                logger.debug(
                    "%s: RESTClient attempt %d/%d %s %s",
                    provider_name, attempt + 1, attempts, method, url,
                )
                r = rest_client.request(
                    method=method,
                    url=url,
                    body=data,
                    headers=headers,
                    _request_timeout=timeout,
                )
                body = r.data
                # Py3: already decoded by RESTClient; Py2: still bytes (== str).
                if isinstance(body, bytes):
                    try:
                        body = body.decode('utf-8')
                    except (UnicodeDecodeError, AttributeError):
                        pass
                return body
            except ApiException as e:
                status = e.status or 0
                err_body = e.body
                if isinstance(err_body, bytes):
                    try:
                        err_body = err_body.decode('utf-8')
                    except (UnicodeDecodeError, AttributeError):
                        err_body = repr(err_body)
                # status == 0 signals a transport-level failure (e.g. SSL,
                # DNS) converted by RESTClient; no point retrying.
                if status == 0:
                    raise RuntimeError(
                        "{}: {} failed at '{}' (transport error): {}".format(
                            provider_name, request_name or "request", url, e.reason,
                        )
                    )
                if retry_on_5xx and status >= 500:
                    last_error = RuntimeError(
                        "HTTP {}: {}".format(status, err_body)
                    )
                    logger.debug(
                        "%s: HTTP %s on attempt %d/%d: %s",
                        provider_name, status, attempt + 1, attempts, err_body,
                    )
                    if attempt < attempts - 1:
                        time.sleep(retry_interval)
                    continue
                raise RuntimeError(
                    "{}: {} failed with HTTP {}: {}".format(
                        provider_name, request_name or "request", status, err_body,
                    )
                )
            except (urllib3.exceptions.HTTPError, IOError, OSError) as e:
                last_error = e
                logger.debug(
                    "%s: transport error on attempt %d/%d: %r",
                    provider_name, attempt + 1, attempts, e,
                )
                if attempt < attempts - 1:
                    time.sleep(retry_interval)

        raise RuntimeError(
            "{}: failed to call {} at '{}' after {} retries: {}".format(
                provider_name, request_name or "request", url, attempts, last_error,
            )
        )

